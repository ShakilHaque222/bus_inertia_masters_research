# =============================================================================
# Helper: build Y_bus from BRANCH_DATA
# =============================================================================
def build_ybus(n_buses=39):
    """
    Build the admittance matrix Y_bus (39x39) from BRANCH_DATA.

    Returns
    -------
    Y_bus : np.ndarray, shape (39,39), complex
    """
    Y = np.zeros((n_buses, n_buses), dtype=complex)
    for branch in BRANCH_DATA:
        i, j = int(branch[0]) - 1, int(branch[1]) - 1  # 0-indexed
        r, x, b = branch[2], branch[3], branch[4]
        z = complex(r, x)
        y = 1.0 / z if abs(z) > 1e-10 else complex(0, 1e6)
        b_shunt = complex(0, b / 2.0)
        Y[i, i] += y + b_shunt
        Y[j, j] += y + b_shunt
        Y[i, j] -= y
        Y[j, i] -= y
    return Y


def get_system_summary():
    """Print a brief summary of the IEEE 39-bus system."""
    S_total = sum(g["S_mva"] for g in GENERATOR_DATA.values())
    H_sys = sum(g["H"] * g["S_mva"] for g in GENERATOR_DATA.values()) / S_total
    E_kin = sum(g["H"] * g["S_mva"] for g in GENERATOR_DATA.values())
    print("=" * 55)
    print("  IEEE 39-Bus New England System")
    print("=" * 55)
    print(f"  Generators  : {len(GENERATOR_DATA)}")
    print(f"  Load buses  : {len(LOAD_BUSES)}")
    print(f"  Branches    : {len(BRANCH_DATA)}")
    print(f"  S_total     : {S_total} MVA")
    print(f"  H_sys (COI) : {H_sys:.3f} s")
    print(f"  E_kin       : {E_kin:.0f} MWs")
    print(f"  VRES sites  : {len(VRES_DATA)}")
    print(f"  GFM inverters: {len(GFM_DATA)}")
    print("=" * 55)
    return H_sys, E_kin
